<article class="hreview open special">
    <h3>Home</h3>
    Zeigt die Startseite an.<br><br>
    <h3>Schüler</h3>
    Listet alle erfassten Schüler auf.<br><br>
    <h3>Schüler erstellen</h3>
    Um einen neuen Schüler zu erstellen müssen Sie zuerst alle Felder ausfüllen und<br> danach auf die "Erstellen" - Taste klicken.<br>
    Vornamen (von 3-50 Zeichen | keine Zahlen)<br>
    Nachnamen (von 3-50 Zeichen | keine Zahlen)<br>
    Email (aufbau : _____@_____.____)  z.b "Peter@Test.com"<br><br>
    <h3>Schüler berabeiten</h3>
    Um einen Schüler zu bearbeiten müssen Sie auf die "bearbeiten" - Taste klicken, danach können Sie de gewünschten Felder bearbeiten und abspeichern mit der "Speicher" - Taste können Sie das ganze abspeichern. <br>Falls Sie einen Schüler aus der Datenbank löschen möchten können Sie einfach auf die "löschen" - taste klicken (aber Achtung!: nach den löschen können Sie den gelöschten Schüler nicht mehr zürücksetzten)
